void main() {
  // var keyword- it is used to decalare variables
  var a1 = 5;
  var a2 = 6;  
  print(a1 + a2);
  // prints output i.e. 11

  // int keyword stores integer
  int a = 2;
  int b = 3;
  print(a + b);

  // dynamic keyword - it automatically detects the input type
  dynamic name = 8;
  print(name);
}
  
