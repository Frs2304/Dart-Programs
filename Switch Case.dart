void main() {
  int n = 3;
  switch (n) {
    case 1:
      print("Value is 1");
      break;
    case 2:
      print("Value is 2");
      break;
    case 3:
      print("Value is 3");
      break;
    case 4:
      print("Value is 4");
      break;
    default:
      print("Out of range");
      break;
  }
}
// Here what will be the value of n,that number of case will be executed
