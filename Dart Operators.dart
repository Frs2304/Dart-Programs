void main() {
  print("Example of Assignment operators");
  var n1 = 10;
  var n2 = 5;
  // addition(+) operator
  print("n1+n2 = ${n1 + n2}");
  // substraction(-) operator
  print("n1-n2 = ${n1 - n2}");
  // multiplication(*) operator
  print("n1*n2 = ${n1 * n2}");
  // division(/) operator  
  print("n1/=n2 = ${n1 / n2}");
  // modulo(%) operator
  print("n1%n2 = ${n1 % n2}");
}
