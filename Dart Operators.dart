void main() {
  print("Example of Assignment operators");
  var n1 = 10;
  var n2 = 5;
  // addition opration
  print("n1+n2 = ${n1 + n2}");
  // substraction opration
  print("n1-n2 = ${n1 - n2}");
  // multiplication opration
  print("n1*n2 = ${n1 * n2}");
  // division opration
  print("n1/=n2 = ${n1 / n2}");
  // modulo opration
  print("n1%n2 = ${n1 % n2}");
}
